-- ============================================================================
-- 1. DASHBOARD PERSONALIZABLE POR USUARIO
-- ============================================================================

-- Configuración de dashboard por usuario: qué widgets ve, en qué orden, tamaño
CREATE TABLE IF NOT EXISTS nm_dashboard_configs (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  club_id int NOT NULL DEFAULT 1,
  user_id uuid NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  dashboard_type text NOT NULL DEFAULT 'player', -- 'admin' | 'player'
  layout jsonb NOT NULL DEFAULT '[]'::jsonb,
  -- layout is array of: { widget_id: string, visible: boolean, order: number,
  --   size: 'sm'|'md'|'lg'|'full', col_span: number, row_span: number,
  --   config: { ... widget-specific settings } }
  theme jsonb DEFAULT '{}'::jsonb,
  -- theme overrides: { accentColor, cardStyle, compactMode, etc. }
  sidebar_collapsed boolean DEFAULT false,
  default_page text, -- which page loads first after login
  quick_actions jsonb DEFAULT '[]'::jsonb,
  -- array of { action_id: string, label: string, visible: boolean, order: number }
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now(),
  UNIQUE(user_id, dashboard_type)
);

-- Widget catalog: define all available widgets with their defaults
CREATE TABLE IF NOT EXISTS nm_dashboard_widgets (
  id text PRIMARY KEY, -- e.g. 'kpi_bookings_today', 'live_cash', 'chart_revenue'
  club_id int NOT NULL DEFAULT 1,
  dashboard_type text NOT NULL DEFAULT 'admin', -- which dashboard this belongs to
  name text NOT NULL,
  description text,
  category text NOT NULL DEFAULT 'general', -- 'kpi' | 'live' | 'chart' | 'list' | 'action'
  default_size text DEFAULT 'md', -- sm | md | lg | full
  default_visible boolean DEFAULT true,
  default_order int DEFAULT 0,
  icon text, -- lucide icon name
  min_role text DEFAULT 'player', -- minimum role to see this widget
  is_active boolean DEFAULT true,
  config_schema jsonb DEFAULT '{}'::jsonb -- JSON schema for widget-specific config
);

