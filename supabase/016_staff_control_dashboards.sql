-- Auto-compute net_minutes on staff_shifts when check_out happens
CREATE OR REPLACE FUNCTION nm_trigger_shift_net_minutes()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
BEGIN
  IF NEW.check_out IS NOT NULL AND NEW.check_in IS NOT NULL THEN
    NEW.net_minutes := GREATEST(0,
      EXTRACT(EPOCH FROM (NEW.check_out - NEW.check_in))::int / 60
      - COALESCE(NEW.break_minutes, 0)
    );
  END IF;
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS tr_nm_shift_net_minutes ON nm_staff_shifts;
CREATE TRIGGER tr_nm_shift_net_minutes
BEFORE UPDATE ON nm_staff_shifts
FOR EACH ROW EXECUTE FUNCTION nm_trigger_shift_net_minutes();

-- Auto-compute gym access duration on checkout
CREATE OR REPLACE FUNCTION nm_trigger_gym_access_duration()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
BEGIN
  IF NEW.check_out_at IS NOT NULL AND NEW.check_in_at IS NOT NULL THEN
    NEW.duration_minutes := GREATEST(0,
      EXTRACT(EPOCH FROM (NEW.check_out_at - NEW.check_in_at))::int / 60
    );
  END IF;
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS tr_nm_gym_access_duration ON nm_gym_access_logs;
CREATE TRIGGER tr_nm_gym_access_duration
BEFORE UPDATE ON nm_gym_access_logs
FOR EACH ROW EXECUTE FUNCTION nm_trigger_gym_access_duration();

-- Log gym staff activity automatically when key tables change
CREATE OR REPLACE FUNCTION nm_trigger_gym_activity_log()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
DECLARE
  v_user uuid;
  v_action text;
  v_desc text;
  v_ref_type text;
  v_ref_id bigint;
BEGIN
  v_user := auth.uid();
  IF v_user IS NULL THEN RETURN NEW; END IF;

  IF TG_TABLE_NAME = 'nm_gym_memberships' THEN
    v_ref_type := 'membership';
    v_ref_id := NEW.id;
    IF TG_OP = 'INSERT' THEN
      v_action := 'membership_change'; v_desc := 'Nueva membresía creada';
    ELSIF TG_OP = 'UPDATE' AND OLD.status != NEW.status THEN
      v_action := 'membership_change'; v_desc := 'Membresía: ' || OLD.status || ' → ' || NEW.status;
    ELSE
      RETURN NEW;
    END IF;
  ELSIF TG_TABLE_NAME = 'nm_gym_sessions' THEN
    v_ref_type := 'session';
    v_ref_id := NEW.id;
    IF TG_OP = 'INSERT' THEN
      v_action := 'check_in'; v_desc := 'Check-in gimnasio';
    ELSIF TG_OP = 'UPDATE' AND NEW.check_out IS NOT NULL AND OLD.check_out IS NULL THEN
      v_action := 'check_out'; v_desc := 'Check-out gimnasio';
    ELSE
      RETURN NEW;
    END IF;
  ELSE
    RETURN NEW;
  END IF;

  INSERT INTO nm_gym_staff_activity (club_id, user_id, action_type, description, reference_type, reference_id)
  VALUES (1, v_user, v_action, v_desc, v_ref_type, v_ref_id);

  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS tr_nm_gym_act_memberships ON nm_gym_memberships;
CREATE TRIGGER tr_nm_gym_act_memberships
AFTER INSERT OR UPDATE ON nm_gym_memberships
FOR EACH ROW EXECUTE FUNCTION nm_trigger_gym_activity_log();

DROP TRIGGER IF EXISTS tr_nm_gym_act_sessions ON nm_gym_sessions;
CREATE TRIGGER tr_nm_gym_act_sessions
AFTER INSERT OR UPDATE ON nm_gym_sessions
FOR EACH ROW EXECUTE FUNCTION nm_trigger_gym_activity_log();

