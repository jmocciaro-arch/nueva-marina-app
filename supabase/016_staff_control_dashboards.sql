-- Enhance nm_staff_shifts: add authentication method and shift type
ALTER TABLE nm_staff_shifts
  ADD COLUMN IF NOT EXISTS auth_method text, -- 'pin' | 'nfc' | 'fingerprint' | 'facial' | 'manual'
  ADD COLUMN IF NOT EXISTS shift_type text DEFAULT 'regular', -- 'regular' | 'extra' | 'replacement'
  ADD COLUMN IF NOT EXISTS break_start timestamptz,
  ADD COLUMN IF NOT EXISTS break_end timestamptz,
  ADD COLUMN IF NOT EXISTS break_minutes int DEFAULT 0,
  ADD COLUMN IF NOT EXISTS net_minutes int, -- total worked minus breaks
  ADD COLUMN IF NOT EXISTS overtime_minutes int DEFAULT 0,
  ADD COLUMN IF NOT EXISTS approved_by uuid REFERENCES auth.users(id),
  ADD COLUMN IF NOT EXISTS approved_at timestamptz,
  ADD COLUMN IF NOT EXISTS ip_address text,
  ADD COLUMN IF NOT EXISTS device_info text;

-- Staff authentication credentials (PIN, NFC, fingerprint, facial)
CREATE TABLE IF NOT EXISTS nm_staff_credentials (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  club_id int NOT NULL DEFAULT 1,
  user_id uuid NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  type text NOT NULL, -- 'pin' | 'nfc' | 'fingerprint' | 'facial'
  credential_data text NOT NULL, -- hashed PIN, NFC tag ID, fingerprint template hash, facial encoding ref
  is_active boolean DEFAULT true,
  created_at timestamptz DEFAULT now(),
  last_used_at timestamptz,
  expires_at timestamptz,
  UNIQUE(user_id, type)
);

CREATE INDEX IF NOT EXISTS idx_nm_staff_cred_type ON nm_staff_credentials(type, credential_data) WHERE is_active = true;

-- Cash shift closings (when staff changes, mandatory cash count)
CREATE TABLE IF NOT EXISTS nm_cash_closings (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  club_id int NOT NULL DEFAULT 1,
  shift_id bigint REFERENCES nm_staff_shifts(id),
  closed_by uuid NOT NULL REFERENCES auth.users(id),
  opened_at timestamptz NOT NULL,
  closed_at timestamptz NOT NULL DEFAULT now(),

  -- Expected amounts (system-calculated from nm_cash_register)
  expected_cash numeric(10,2) DEFAULT 0,
  expected_card numeric(10,2) DEFAULT 0,
  expected_transfer numeric(10,2) DEFAULT 0,
  expected_bizum numeric(10,2) DEFAULT 0,
  expected_total numeric(10,2) DEFAULT 0,

  -- Actual counted amounts (staff enters these)
  actual_cash numeric(10,2) DEFAULT 0,
  actual_card numeric(10,2) DEFAULT 0,
  actual_transfer numeric(10,2) DEFAULT 0,
  actual_bizum numeric(10,2) DEFAULT 0,
  actual_total numeric(10,2) DEFAULT 0,

  -- Differences
  diff_cash numeric(10,2) GENERATED ALWAYS AS (actual_cash - expected_cash) STORED,
  diff_card numeric(10,2) GENERATED ALWAYS AS (actual_card - expected_card) STORED,
  diff_transfer numeric(10,2) GENERATED ALWAYS AS (actual_transfer - expected_transfer) STORED,
  diff_bizum numeric(10,2) GENERATED ALWAYS AS (actual_bizum - expected_bizum) STORED,
  diff_total numeric(10,2) GENERATED ALWAYS AS (actual_total - expected_total) STORED,

  -- Counts
  total_transactions int DEFAULT 0,
  total_bookings int DEFAULT 0,
  total_sales int DEFAULT 0,

  notes text,
  status text DEFAULT 'open', -- 'open' | 'closed' | 'reviewed' | 'approved'
  reviewed_by uuid REFERENCES auth.users(id),
  reviewed_at timestamptz,

  created_at timestamptz DEFAULT now()
);

-- Stock snapshots at shift change
CREATE TABLE IF NOT EXISTS nm_stock_snapshots (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  club_id int NOT NULL DEFAULT 1,
  cash_closing_id bigint REFERENCES nm_cash_closings(id),
  taken_by uuid NOT NULL REFERENCES auth.users(id),
  taken_at timestamptz DEFAULT now(),
  items jsonb NOT NULL DEFAULT '[]'::jsonb,
  -- items: [{ product_id, product_name, expected_stock, actual_stock, difference }]
  total_products int DEFAULT 0,
  products_with_diff int DEFAULT 0,
  notes text,
  status text DEFAULT 'pending' -- 'pending' | 'reviewed' | 'approved'
);

-- Shift handover log (complete record of what happened during handover)
CREATE TABLE IF NOT EXISTS nm_shift_handovers (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  club_id int NOT NULL DEFAULT 1,
  outgoing_user_id uuid NOT NULL REFERENCES auth.users(id),
  incoming_user_id uuid NOT NULL REFERENCES auth.users(id),
  outgoing_shift_id bigint REFERENCES nm_staff_shifts(id),
  incoming_shift_id bigint REFERENCES nm_staff_shifts(id),
  cash_closing_id bigint REFERENCES nm_cash_closings(id),
  stock_snapshot_id bigint REFERENCES nm_stock_snapshots(id),
  handover_at timestamptz DEFAULT now(),
  notes text,
  -- Checklist items completed
  checklist jsonb DEFAULT '[]'::jsonb,
  -- [{ item: 'Caja cuadrada', done: true }, { item: 'Stock verificado', done: true }, ...]
  status text DEFAULT 'completed', -- 'in_progress' | 'completed' | 'flagged'
  created_at timestamptz DEFAULT now()
);

